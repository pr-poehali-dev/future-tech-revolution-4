import { writeFileSync } from 'fs';
import { resolve } from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));

const header = Buffer.from([
  0x00,0x00, 0x01,0x00, 0x01,0x00
]);

const dirEntry = Buffer.from([
  0x10, 0x10, 0x00, 0x00,
  0x01,0x00, 0x20,0x00,
  0x68,0x04,0x00,0x00,
  0x16,0x00,0x00,0x00
]);

const bmpHeader = Buffer.from([
  0x28,0x00,0x00,0x00,
  0x10,0x00,0x00,0x00,
  0x20,0x00,0x00,0x00,
  0x01,0x00,
  0x20,0x00,
  0x00,0x00,0x00,0x00,
  0x00,0x04,0x00,0x00,
  0x00,0x00,0x00,0x00,
  0x00,0x00,0x00,0x00,
  0x00,0x00,0x00,0x00,
  0x00,0x00,0x00,0x00
]);

const row = Buffer.alloc(16 * 4);
for (let i = 0; i < 16; i++) {
  row[i * 4 + 0] = 0xED; // B
  row[i * 4 + 1] = 0x3A; // G
  row[i * 4 + 2] = 0x7C; // R
  row[i * 4 + 3] = 0xFF; // A
}
const pixels = Buffer.concat(Array(16).fill(row));
const andMask = Buffer.alloc(64, 0x00);

const ico = Buffer.concat([header, dirEntry, bmpHeader, pixels, andMask]);
const outPath = resolve(__dirname, '../public/favicon.ico');
writeFileSync(outPath, ico);
console.log('favicon.ico created at', outPath, ico.length, 'bytes');
